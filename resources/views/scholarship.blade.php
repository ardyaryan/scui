                <ul style="margin-left: 20px;">
                    <li>
                        <h3>1</h3>
                        <p style="margin: -21px 0 0 30px;">
                            Students must demonstrate a high degree of critical thinking communication skills.
                            <br><br>
                            &rarr; Students must be able to communicate effectively in the written and oral form and in a way that is appropriate for the existing objectives and audience.<br>
                            &rarr; Students must be able to evaluate information, its sources, critical issues, and problems, and offer a range of problem-solving strategies.<br>
                            &rarr; Students must have the ability to integrate selected information into their knowledge base.<br>
                            &rarr; Students must be able to analyze issues critically using their newly acquired knowledge base and predict possible consequences in the potential implementation of solutions.<br>
                        </p>
