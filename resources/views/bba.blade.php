                <h3 class="heading-agileinfo" style="text-align: left;font-size: 25px !important;margin-bottom: 10px;">Mission, Statement and Program Objectives</h3>
                <p>The Master of Business Administration at SCIU offers an ambitious curriculum that puts students in the middle of the emerging global economy. It provides a framework for understanding markets and the international operations of companies. The SCIU MBA program helps students to develop the practical skills needed to operate a business effectively in a world where international competition thrives.
                    The mission of the MBA program is to provide students with a high-quality Masters-level education in the field of business administration. The program is designed to prepare students for positions of increasing responsibility and leadership in the business and public administration fields.</p>
                <br>
                <h4>The SCIU MBA Program has the following objectives:</h4>
                <br>
                <ul style="margin-left: 20px;">
                    <li>
                        <h3>1</h3>
                        <p style="margin: -21px 0 0 30px;">
                            Students must demonstrate a high degree of critical thinking communication skills.
                            <br><br>
                            &rarr; Students must be able to communicate effectively in the written and oral form and in a way that is appropriate for the existing objectives and audience.<br>
                            &rarr; Students must be able to evaluate information, its sources, critical issues, and problems, and offer a range of problem-solving strategies.<br>
                            &rarr; Students must have the ability to integrate selected information into their knowledge base.<br>
                            &rarr; Students must be able to analyze issues critically using their newly acquired knowledge base and predict possible consequences in the potential implementation of solutions.<br>
                        </p>
                    </li><br>
                    <li>
                        <h3>2</h3>
                        <p style="margin: -21px 0 0 30px;">Students must be able to apply theoretical and practical skills in dealing with the myriad of potential issues in the corporate decision making process.
                            <br><br>
                            &rarr; Students must be able to assess the importance and development of organizational human and business issues.<br>
                            &rarr; Students must be able to identify, describe, and explain a range of corporate processes, procedures, policies and concepts that ensure the smooth functioning of small and large organizations.<br>
                            &rarr; Students must be able to assess the impact of outside forces such as the competition, interested stakeholders and the public interest aspect. This might include legal, ethical, social, economic, technological and international issues.<br>
                        </p>
                    </li>
                    <br>
                    <li>
                        <h3>3</h3>
                        <p style="margin: -21px 0 0 30px;">
                            Students must be able to demonstrate collaboration and teamwork in the learning and problem- solving process.
                            <br><br>
                            &rarr; Students must be able to identify, describe, and discuss perspectives and best practices with regards to a team-based approach to leadership in an organization.<br>
                            &rarr; Students must be able to apply a modicum of interpersonal and leadership skills in team-based learning and work activities.
                        </p>
                    </li>
                    <br>
                </ul>
                <br>
                <h4>Expected Learning Outcomes:</h4>
                <br>
                <ul style="margin-left: 20px;">
                    <li>
                        <h3>1</h3>
                        <p style="margin: -21px 0 0 30px;">Critically analyze an industry and a specific firm within an industry.</p>
                    </li><br>
                    <li>
                        <h3>2</h3>
                        <p style="margin: -21px 0 0 30px;">Synthesize important relationships across business disciplines.</p>
                    </li>
                    <br>
                    <li>
                        <h3>3</h3>
                        <p style="margin: -21px 0 0 30px;">Interpret ethical and legal concerns of stakeholders and be able to comply with codes and regulations.</p>
                    </li>
                    <li>
                        <h3>4</h3>
                        <p style="margin: -21px 0 0 30px;">Communicate effectively, both verbally and in writing, the ideas and arguments associated with business issues.</p>
                    </li>
                    <li>
                        <h3>5</h3>
                        <p style="margin: -21px 0 0 30px;">Explain and apply analytical constructs to business problem solving.</p>
                    </li>
                    <li>
                        <h3>6</h3>
                        <p style="margin: -21px 0 0 30px;">Design organizations to fit the specific business issues and formulate reasonable and logical implementation decisions.</p>
                    </li>
                    <li>
                        <h3>7</h3>
                        <p style="margin: -21px 0 0 30px;">Recognize and understand the special opportunities and challenges presented by the global business environment.</p>
                    </li>
