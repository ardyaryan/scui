                <h3 class="heading-agileinfo" style="text-align: left;font-size: 25px !important;margin-bottom: 10px;">Mission, Statement and Program Objectives</h3>
                <p>The mission of the MBA program is to provide academically rigorous instruction in traditional, contemporary, and emerging management theory, research, and practice, and by doing so, to stimulate student interest in management studies and the management profession. The program intends to teach students to understand the nature and structure of organizations, and to contribute both support an d leadership in the development of skill sets. Our particular focus on skills is in the areas of management and leadership, human relations, strategy, collaboration, and communication.</p>
                <br>
                <h4>SCIU’s objectives for the MBA program are the following:</h4>
                <br>
                <ul style="margin-left: 20px;">
                    <li>
                        <h3>1</h3>
                        <p style="margin: -21px 0 0 30px;">To provide course experiences and assignments which will help develop students’ proficiency in basic academic skills of writing, speaking, and critical thinking.</p>
                    </li><br>
                    <li>
                        <h3>2</h3>
                        <p style="margin: -21px 0 0 30px;">To provide an orientation to management that emphasizes international, cultural, ethical, legal, organizational-process, and economic perspectives.</p>
                    </li>
                    <br>
                    <li>
                        <h3>3</h3>
                        <p style="margin: -21px 0 0 30px;">To familiarize students with the role of uncertainty, limited information, risk, and conflict in management decision making, so as to help them build an appreciation for ambiguity and prioritization.</p>
                    </li>
                    <li>
                        <h3>4</h3>
                        <p style="margin: -21px 0 0 30px;">To provide opportunities for students to analyze critically and to evaluate the assumptions that they make about people and their behavior in organizations.</p>
                    </li>
                    <li>
                        <h3>5</h3>
                        <p style="margin: -21px 0 0 30px;">To offer relevant experience in team work and group-based learning and problem-solving.</p>
                    </li>
                    <br>
                </ul>
                <br>
                <h4>Expected Learning Outcomes:</h4>
                <br>
                <ul style="margin-left: 20px;">
                    <li>
                        <h3>1</h3>
                        <p style="margin: -21px 0 0 30px;">Demonstrate basic competency in oral and written communication (defined as writing which is clear, concise, well organized, well reasoned).</p>
                    </li><br>
                    <li>
                        <h3>2</h3>
                        <p style="margin: -21px 0 0 30px;">Demonstrate critical thinking skills in case analyses and other written and oral communication exercises.</p>
                    </li>
                    <br>
                    <li>
                        <h3>3</h3>
                        <p style="margin: -21px 0 0 30px;">Demonstrate understanding of managerial issues and problems related to the global economy and international business.</p>
                    </li>
                    <li>
                        <h3>4</h3>
                        <p style="margin: -21px 0 0 30px;">Demonstrate a basic grasp of how the legal system and regulatory system affect management decision making.</p>
                    </li>
                    <li>
                        <h3>5</h3>
                        <p style="margin: -21px 0 0 30px;">Demonstrate familiarity with social responsibility issues that managers must address, including (but not limited to) business ethics, cultural diversity, and environmental concerns.</p>
                    </li>
                    <li>
                        <h3>6</h3>
                        <p style="margin: -21px 0 0 30px;">Demonstrate understanding of organizational conflict and conflict resolution, power, and organizational politics, so as to cope effectively with these processes when confronted in "real- world" business and managerial situations.</p>
                    </li>
                    <br>
                </ul>

